#include <unistd.h>

void    print_bits(unsigned char octet)
{
    int z = 128;
    int oct = octet;

    while (z > 0)
    {
        if (oct & z)
            write(1, "1", 1);
        else
            write(1, "0", 1);
        z >>= 1;
    }
}

int     main()
{
    print_bits(42);
    return (0);
}

//42 = 00101010

//128 & 42  10000000 00101010 == 0
//64 & 42   01000000 00101010 == 0
